def add(a, b, c=1):
    """
    Return a + b + c.

    :param a: value a.
    :type a: int or float
    :param b: value b.
    :type b: int or float
    :param c: optional value c.
    :type c: int or float or None
    :return: sum of a b c.
    :rtype: int or float
    """
    return a + b + c
